let currentLang = 'ja';

function t(key) {
    return (I18N[currentLang] && I18N[currentLang][key]) || (I18N.ja[key]) || key;
}

// settings.js の ACTION_LABELS を i18n 経由で参照するためのアクセサ
function tAction(actionId) {
    const dict = I18N_ACTION_LABELS[currentLang] || I18N_ACTION_LABELS.ja;
    return dict[actionId] || actionId;
}

function applyI18n() {
    // data-i18n または data-i18n-keep の両方を拾う
    document.querySelectorAll('[data-i18n], [data-i18n-keep]').forEach(el => {
        const key = el.dataset.i18n || el.dataset.i18nKeep;
        if (!key) return;
        if (el.dataset.i18nKeep) {
            // 最初のテキストノードのみ置換（menu-key span 等の子要素を維持）
            let replaced = false;
            for (let n of el.childNodes) {
                if (n.nodeType === 3) { n.nodeValue = t(key); replaced = true; break; }
            }
            if (!replaced) el.insertBefore(document.createTextNode(t(key)), el.firstChild);
        } else {
            el.innerText = t(key);
        }
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        el.innerHTML = t(el.dataset.i18nHtml);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.dataset.i18nPlaceholder);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        el.title = t(el.dataset.i18nTitle);
    });
}

function setLang(lang) {
    if (!I18N[lang]) return;
    currentLang = lang;
    try { localStorage.setItem('webTSEditor.lang', lang); } catch (e) {}
    applyI18n();
    // 動的に組まれるUIも再描画
    if (typeof updateModeStatus === 'function') updateModeStatus();
    if (typeof drawAll === 'function') drawAll();
}

function loadLang() {
    try {
        const saved = localStorage.getItem('webTSEditor.lang');
        if (saved && I18N[saved]) currentLang = saved;
    } catch (e) {}
}
